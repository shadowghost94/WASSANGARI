function showVisiterDetails(eventId) {
    const modal = document.getElementById("visterSite" + eventId.toString());
    const ensemble = document.querySelectorAll(".carousel-container-visiter");
    const lesH3 = document.querySelectorAll('h3');
    const PourTous = document.getElementById('PourTous');
    const body = document.body;

    ensemble.forEach(ensemb =>{
        ensemb.style.display="none";
    });
    lesH3.forEach(h3 => {
        h3.style.display="none";
    })

    PourTous.appendChild(modal);
    modal.style.display="block";
};

function closeVisiterDetails(eventId) {
    const modal = document.getElementById("visterSite" + eventId.toString());
    const ensemble = document.querySelectorAll(".carousel-container-visiter");
    const lesH3 = document.querySelectorAll('h3');
    const PourTous = document.getElementById('PourTous');
    const body = document.body;
    
    ensemble.forEach(ensemb => {
        ensemb.style.display = "block";
    });
    lesH3.forEach(h3 => {
        h3.style.display = "block";
    });

    // Enregistre l'emplacement d'origine du modal pour pouvoir le restaurer plus tard
    modal.originalParent = modal.parentElement;
    PourTous.appendChild(modal);
    modal.style.display = "none";
}


